    def __init__(self, mdp, discount = 0.9, iterations = 1000):
        """
          Your cyclic value iteration agent should take an mdp on
          construction, run the indicated number of iterations,
          and then act according to the resulting policy. Each iteration
          updates the value of only one state, which cycles through
          the states list. If the chosen state is terminal, nothing
          happens in that iteration.

          Some useful mdp methods you will use:
              mdp.getStates()
              mdp.getPossibleActions(state)
              mdp.getTransitionStatesAndProbs(state, action)
              mdp.getReward(state)
              mdp.isTerminal(state)
        """
        ValueIterationAgent.__init__(self, mdp, discount, iterations)

    def runValueIteration(self):
        "*** YOUR CODE HERE ***"
        states = self.mdp.getStates()
        statesNumber = len(states)
        # counter only need to initialize once
        counter = util.Counter()
        for iteration in range(0, self.iterations):
            # calculate index
            index = iteration % statesNumber
            curState = states[index]
            if not self.mdp.isTerminal(curState):
                #update state value
                counter[curState] = self.computeQValueFromValues(curState, self.computeActionFromValues(curState))
            # update self.values for next iteration
            self.values = counter.copy()

